# 카메라 이름은 "CCTV-NN (구역 N)" 형식으로 자동 생성됩니다 (1~MAX_CAMERAS번).
# 카메라 개수는 이 함수로 직접 조절하지 않고 PRESET_OUTPOSTS(초소 프리셋)의
# 개수로 결정됩니다 (services/outposts.py 참고). 이 함수는 PRESET_OUTPOSTS가
# 비어있는 예외 상황에서 화면이 완전히 비지 않도록 하는 기본 카메라 1개
# 폴백 용도로만 남아 있습니다.
MAX_CAMERAS = 16  # 카메라 개수 상한(폴백 계산 시 사용)


def build_camera_list(count: int) -> list[dict]:
    """요청된 개수(count)만큼 카메라 슬롯 딕셔너리 리스트를 생성합니다.

    count가 1 미만이면 1로, MAX_CAMERAS 초과면 MAX_CAMERAS로 자동 보정됩니다.
    각 카메라는 {"id": "camN", "name": "표시용 이름"} 형태입니다.
    """
    count = max(1, min(count, MAX_CAMERAS))
    cams = []
    for i in range(count):
        name = f"CCTV-{i+1:02d} (구역 {i+1})"
        cams.append({"id": f"cam{i+1}", "name": name})
    return cams
